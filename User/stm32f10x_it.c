char rx_buf_SIM[255];
uint8_t num_SIM = 0;

// SIM800串口中断服务函数
void SIM800_USART_IRQHandler(void)
{
    if(USART_GetITStatus(SIM800_USARTx,USART_IT_RXNE)!=RESET)
    {
        rx_buf_SIM[num_SIM] = USART_ReceiveData(SIM800_USARTx);
		Usart_SendByte(DEBUG_USARTx, rx_buf_SIM[num_SIM]);
		num_SIM++;	
		rx_buf_SIM[num_SIM] = '\0';
    }
}

